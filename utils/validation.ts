export const determineTypeSim = (id) => {
  if (/^(26|27|28|29|30|31|32)\d{4,}$/.test(id)) {
    return "physical";
  } else if (/^(78|79|80)\d{4,}$/.test(id)) {
    return "electronic";
  } else {
    return false;
  }
};
